package days.day18;

public class ForLoop {

    public static void main(String[]args){

        // 0 dan 5 e kadar dongu devam etsin
        // for // keyword
        // statement 1 - initialization // int i =0;
        // statement 2 - Condition // i < 5;
        // statement 3 - inc/dec  // i++
        // statement - code execution // {}

        System.out.println("Döngü baslangici");

        for (int i=0;i<5;i++){

            System.out.println("Hello World");
        }
        System.out.println("Döngü sonu");
    }
}
