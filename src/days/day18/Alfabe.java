package days.day18;

public class Alfabe {
    public static void main(String[] args) {
        // alfabenin harflerini forloop kullanarak yazdiriniz

        char harf = 'A';
        for (harf = 'A'; harf <= 'Z'; harf++) {
            int i = harf;
            System.out.println(harf + " = " + i);


        }
    }
}
