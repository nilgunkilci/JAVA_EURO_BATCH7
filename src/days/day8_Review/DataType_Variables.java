package days.day8_Review;

public class DataType_Variables {
    public static void main(String[] args) {

        // declaration
        byte b;
        char ch;
        int i;
        double d;

        // assignment
        b = 5;
        ch = 'A';
        i = 500;
        d = 25.5;

        // declaration + assignment
        int i2 = 600;
        float f = 15.7f;

        // multipable declaration
        int num1, num2, num3;

        num1 = 5;
        num2 = 10;
        num3 = 20;

        // multiple declaration + assignment
        int num4 = 15, num5 = 25, num6 = 35;


        int sayi;
        sayi = 1;
        System.out.println("sayi = " + sayi);
        // int sayi;
        sayi = 2;
        System.out.println("sayi = " + sayi);
        sayi = 3;
        System.out.println("sayi = " + sayi);

        double d5 = 10.;
        System.out.println("d5 = " + d5);

        double dd = 10d;
        System.out.println("dd = " + dd);

        String str = "akslkaj";
        String str2 = "akdfka124124&^%$££@";
        String str3 = " sdgsdg sfef" + " dgihoahd87945";
        System.out.println("str3 = " + str3);

        char ch1 = 'r';
        System.out.println("ch1 = " + ch1);

        int sayiDegeri = ch1;
        System.out.println("sayiDegeri = " + sayiDegeri);

        final double pi = 3.14;
        // pi = 3.15;
    }
}
