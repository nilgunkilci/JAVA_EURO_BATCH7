package days.day22;

public class C3_BiggerNumber {
    public static void main(String[] args) {
        biggerNumber();


    }

    public static void biggerNumber() {
        int num1 = 10;
        int num2 = 5;
        if (num1 > num2) {
            System.out.println(num1 + " is bigger number");
        } else {
            System.out.println(num2 + " is bigger number");
        }
    }
}
