package days.day9;

public class ÖdevAlistirmalar33 {
    public static void main(String[] args) {

        // Örnek 37     2 + 2 + 3 + 4

        int örnek37 = 2 + 2 + 3 + 4;
        System.out.println("örnek37 = " + örnek37); //  sonuc 11

        //___________________________________________________________________

        // Örnek 38      "2 + 2 " + 3 + 4

        String örnek38 = "2 + 2 " + 3 + 4;
        System.out.println("örnek38 = " + örnek38); // 2 + 2 34

        //____________________________________________________________

        // Örnek 39       2 + " 2 + 3 " + 4

        String örnek39 = 2 + " 2 + 3 " + 4;
        System.out.println("örnek39 = " + örnek39); // 2 2 + 3 4

        //_________________________________________________________________

        // Örnek 40       3 + 4 + " 2 + 2"

        String örnek40 = 3 + 4 + " 2 + 2";
        System.out.println("örnek40 = " + örnek40); // 7 2 + 2

        //____________________________________________________________

        // Örnek 41         "2 + 2 " + (3 + 4)

        String örnek41 = "2 + 2 " + (3 + 4);
        System.out.println("örnek41 = " + örnek41); // 2 + 2 7

        //____________________________________________________________________

        //Örnek 42          "(2 + 2) " + (3 < 4)

        String örnek42 = "(2 + 2) " + (3 < 4);
        System.out.println("örnek42 = " + örnek42); // (2 + 2) true

        //_____________________________________________________________________________

        // Örnek 43              "hello 34 " + 2 * 4

        String örnek43 = "hello 34 " + 2 * 4;
        System.out.println("örnek43 = " + örnek43); //hello 34 8

        //_____________________________________________________________________

        // Örnek 44             2 + "(int) 2.0" + 2 * 2 + 2

        String örnek44 = 2 + "(int) 2.0" + 2 * 2 + 2;
        System.out.println("örnek44 = " + örnek44); // 2(int) 2.042

        //______________________________________________________________________________

        // Örnek 45             4 + 1 + 9 + "." + (-3 + 10) + 11 / 3

        String örnek45 = 4 + 1 + 9 + "." + (-3 + 10) + 11 / 3;
        System.out.println("örnek45 = " + örnek45); // 14.73

        //_________________________________________________________________________________

        // Örnek 46 8 + 6 * -2 + 4 + "0" + (2 + 5)

        String örnek46 = 8 + 6 * -2 + 4 + "0" + (2 + 5);
        System.out.println("örnek46 = " + örnek46); // 007

        //_________________________________________________________________________________

        //


    }
}
