package days.day17;

public class While2 {
    public static void main(String[] args) {

        int a=0;
        while (a<5){
            a+=2;
            System.out.println(a);

            System.out.println("Hello");
        }
        System.out.println(a);
        System.out.println("____________________________________");
        a=5;
        while (a>0){
            System.out.println(a);
            a--;


        }
        System.out.println(a);
    }
}
